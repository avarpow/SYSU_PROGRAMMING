/*
Description
给定一个n*n矩阵，计算对角线上数字的和。

Input
第一行是一个整数 n (1 <= n <= 10), 表示矩阵的大小, 接下来是一个 n * n 的矩阵, 都是整数

Output
输出对角线的和

Sample Input
4
1 2 3 4
5 6 7 8
8 7 6 5
4 3 2 1
Sample Output
14
*/
#include<stdio.h>
int main()
{
    int n,ans=0,t;
    scanf("%d", &n);
    for (int i = 0; i < n*n;i++){
        scanf("%d", &t);
		if (i % (n + 1) == 0)
			ans = ans + t;
    }
    printf("%d\n", ans);
}
